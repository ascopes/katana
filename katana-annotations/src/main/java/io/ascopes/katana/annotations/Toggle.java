package io.ascopes.katana.annotations;

/**
 * Toggle for some generic functionality.
 *
 * @author Ashley Scopes
 * @since 0.0.1
 */
public enum Toggle {
  /**
   * Enable the functionality.
   */
  ENABLED,

  /**
   * Disable the functionality.
   */
  DISABLED,

  /**
   * Inherit the setting.
   */
  INHERITED,
}
